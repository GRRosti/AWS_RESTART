"""
Dictionaries, lists, touples
"""
my_list = ["Apple","Banana","Cherry"]
print(my_list[0])
print(my_list[1])
print(my_list[2])
my_list[2]="Orange"
print(my_list)
print(type(my_list))

my_touple = ("apple", "banana", "pineapple")
print(my_touple)
print(type(my_touple))
print(my_touple[0])
print(my_touple[1])
print(my_touple[2])

my_dictionary = {
    "Akua" : "apple",
  "Saanvi" : "banana",
  "Paulo" : "pineapple"
}
print(my_dictionary)
print(type(my_dictionary))
print(my_dictionary["Akua"])
print(my_dictionary["Saanvi"])
print(my_dictionary["Paulo"])
